<h2><a href="http://blog.getfirebug.com/2009/12/17/firebug-1-5b7/" rel="bookmark" title="Permanent Link to Firebug 1.5b7">Firebug 1.5b7</a></h2>
<div class="postDate">December 17th, 2009 by johnjbarton </div>

<div class="entry">
<p>getfirebug.com has <a href="http://getfirebug.com/releases">Firebug 1.5X.0b7</a>.  We plan to release 1.5.0 in the first week of 2010. Happy New Year!</p>
<h2>Test results:</h2>
<ul>
<li>On Firefox 3.5.6, one test fails, Issue <a href="http://code.google.com/p/fbug/issues/detail?id=2271">2271</a>, because we use a feature only available in Firefox 3.6.</li>
<li>On Firefox 3.6b6pre, one test fails, because of <a href="https://bugzilla.mozilla.org/show_bug.cgi?id=468506"> <strong>Bug 468506</strong></a> a Firefox bug that is blocking the release of Firefox 3.6.</li>

<li>On Firefox 3.7 nightly, three tests fail.One is a known problem in our tests, the other two are caused by <a title="NEW - Permission denied for &lt;https://getfirebug.com&gt; to get property Function.__parent__ from &lt;&gt;" href="https://bugzilla.mozilla.org/show_bug.cgi?id=535699">bug 535699</a>.</li>
</ul>
<h2>New in this release:</h2>
<ul>
<li>Turn off the Javascript Debugger service of Firefox if you don&#8217;t have Firebug open on any tab in Firefox.</li>
<li>When suspending, the JIT is re-enabled. <strong>Thanks</strong> to Boris Zbarsky and the Firefox team for adding support for this improvement!</li>
<li>Performance improvements by leveraging native function getElementsByClassName.</li>

<li>More content types for JSON tab</li>
<li>Improved support for the Cache tab</li>
<li>Duplicate error messages prefixed with count, eg 5x.</li>
<li>Better precision on the file sizes in the Net panel</li>
<li>New Locale, el-GR by Sonickydon</li>
<li>Locale updates: sl-SI, vi-VN, ja-JP, es-AR, sv-SE, ca-CD, pt-PT, zh-CN, ru-RU, nl-NL, de-DE</li>
</ul>
<p>We are fixing lots bugs so I think enumerating them in the blog here is not so useful. In future posts I will give a link like this one<a href="http://code.google.com/p/fbug/issues/list?can=1&amp;q=label%3AFixed-1.5b7">:</a></p>
<p><a href="http://code.google.com/p/fbug/issues/list?can=1&amp;q=label%3AFixed-1.5b7">Bugs Fixed in 1.5b7</a></p>

<p>jjb</p>
